// Declaración de una matriz bidimensional de números en TypeScript
let matriz: number[][] = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
  ];
  
  // Acceso al elemento en la segunda fila, tercera columna (índice 1, 2)
  console.log(matriz[1][2]); // Salida: 6
  
  // Declaración de una matriz unidimensional de números en TypeScript
